        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollno=jTextField1.getText();
        String name=jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String father=jTextField3.getText();
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clg","root","Keerthika@21");
            
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branch,name,gender,father) values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+father+"')");
            JOptionPane.showMessageDialog(null, "Data Updated Successfully!");
            setVisible(false);
            new index().setVisible(true);
        }
